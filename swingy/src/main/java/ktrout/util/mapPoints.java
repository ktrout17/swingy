package ktrout.util;

public class mapPoints {
	
	private int x;
	private int y;
	
	public mapPoints(int x, int y) {
		x = this.x;
		y = this.y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setX(int x) {
		x = this.x;
	}
	
	public void setY(int y) {
		y = this.y;
	}
}
